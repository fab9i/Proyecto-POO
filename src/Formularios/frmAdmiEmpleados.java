    void listarEmpleados(){
        modelo.setRowCount(0);
        for (int i = 0; i < gestionEmpleados.obtenerTotalEmpleados(); i++) {
            //crear filas
            Object[] fila = {
                gestionEmpleados.obtenerEmpleados()[i].getDni(),
                gestionEmpleados.obtenerEmpleados()[i].getNombre(),
                gestionEmpleados.obtenerEmpleados()[i].getApellido(),
                gestionEmpleados.obtenerEmpleados()[i].getUsuario(),
                gestionEmpleados.obtenerEmpleados()[i].getContrasena(),
                gestionEmpleados.obtenerEmpleados()[i].getRol(),
                
                
            };
            modelo.addRow(fila);
            
        }
    
    }
    
