    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String codigo = txtCodigo.getText().trim();
        Vehiculo vehiculo = gestionVehiculos.buscarPorCodigo(codigo);
        
        if(vehiculo != null){
            txtMarca.setText(vehiculo.getMarca());
            txtModelo.setText(vehiculo.getModelo());
            txtColor.setText(vehiculo.getColor());
            txtAñoFabricacion.setText(String.valueOf(vehiculo.getAnioFabricacion()));
            txtTipo.setText(vehiculo.getTipo());
            txtPrecio.setText(String.format("%.2f", vehiculo.getPrecio()));
            txtEstado.setText(vehiculo.getEstado());
        
        }
        else{
            JOptionPane.showMessageDialog(this, "Vehiculo no encontrado");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String codigo = txtCodigo.getText().trim();
        String marca = txtMarca.getText().trim();
        String color = txtColor.getText().trim();
        String modelo = txtModelo.getText().trim();
        String añoFabricacion = txtAñoFabricacion.getText().trim();
        String tipo = txtTipo.getText().trim();
        String precio = txtPrecio.getText().trim();
        String estado = txtEstado.getText().trim();
        
        Vehiculo vehiculo = new Vehiculo(codigo, marca, modelo, color, Integer.parseInt(añoFabricacion), tipo, Double.parseDouble(precio), estado);
        if(gestionVehiculos.registar(vehiculo)){
            listarVehiculos();
            limpiarCampos();
        
        }
        else{
            JOptionPane.showMessageDialog(this, "No se pudo registrar");
        }
        
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        String codigo = txtCodigo.getText().trim();
        String marca = txtMarca.getText().trim();
        String color = txtColor.getText().trim();
        String modelo = txtModelo.getText().trim();
        String añoFabricacion = txtAñoFabricacion.getText().trim();
        String tipo = txtTipo.getText().trim();
        String precio = txtPrecio.getText().trim();
        String estado = txtEstado.getText().trim();
        
        Vehiculo vehiculo = new Vehiculo(codigo, marca, modelo, color, Integer.parseInt(añoFabricacion), tipo, Double.parseDouble(precio), estado);
        if(gestionVehiculos.modificar(vehiculo)){
            listarVehiculos();
            JOptionPane.showMessageDialog(this, "Curso modificado");
        
        }
        else{
            JOptionPane.showMessageDialog(this, "No se encontró el vehiculo a modificar");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String codigo = txtCodigo.getText().trim();
        
        if(gestionVehiculos.eliminar(codigo)){
            listarVehiculos();
            limpiarCampos();
            JOptionPane.showMessageDialog(this, "Curso eliminado");
        
        }
        else{
            JOptionPane.showMessageDialog(this, "No se encontró el vehiculo a eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

