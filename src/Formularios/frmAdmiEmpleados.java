    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String dni = txtDNI.getText().trim();
        String nombre = txtNombre.getText().trim();
        String apellido = txtApellido.getText().trim();
        String usuario = txtUsuario.getText().trim();
        String contraseña = txtContraseña.getText().trim();
        String rol = txtRol.getText().trim();
        

        Empleado empleado = new Empleado(dni, nombre, apellido, usuario, contraseña, rol);
        if(gestionEmpleados.registar(empleado)){
            listarEmpleados();
            limpiarCampos();

        }
        else{
            JOptionPane.showMessageDialog(this, "No se pudo registrar");
        }

    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
       String dni = txtDNI.getText().trim();
        String nombre = txtNombre.getText().trim();
        String apellido = txtApellido.getText().trim();
        String usuario = txtUsuario.getText().trim();
        String contraseña = txtContraseña.getText().trim();
        String rol = txtRol.getText().trim();
        

        Empleado empleado = new Empleado(dni, nombre, apellido, usuario, contraseña, rol);
        if(gestionEmpleados.modificar(empleado)){
            listarEmpleados();
            JOptionPane.showMessageDialog(this, "Empleado modificado");

        }
        else{
            JOptionPane.showMessageDialog(this, "No se encontró el empleado a modificar");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String dni = txtDNI.getText().trim();

        if(gestionEmpleados.eliminar(dni)){
            listarEmpleados();
            limpiarCampos();
            JOptionPane.showMessageDialog(this, "Empleado eliminado");

        }
        else{
            JOptionPane.showMessageDialog(this, "No se encontró el empleado a eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String DNI = txtDNI.getText().trim();
        Empleado empleado = gestionEmpleados.buscarPorDNI(DNI);

        if(empleado != null){
            txtDNI.setText(empleado.getDni());
            txtNombre.setText(empleado.getNombre());
            txtApellido.setText(empleado.getApellido());
            txtUsuario.setText(empleado.getUsuario());
            txtContraseña.setText(empleado.getContrasena());
            txtRol.setText(empleado.getRol());

        }
        else{
            JOptionPane.showMessageDialog(this, "Empleado no encontrado");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

