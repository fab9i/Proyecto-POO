    void listarClientes() {
        modelo.setNumRows(0);
        for (int i = 0; i < gestionClientes.obtenerTotalClientes(); i++){
            
            Object[] fila ={
                gestionClientes.obtenerClientes()[i].getDni(),
                gestionClientes.obtenerClientes()[i].getNombre(),
                gestionClientes.obtenerClientes()[i].getApellido(),
                gestionClientes.obtenerClientes()[i].getDireccion(),
                gestionClientes.obtenerClientes()[i].getCelular(),
                gestionClientes.obtenerClientes()[i].getCorreoElectronico(),
               
            };
            modelo.addRow(fila);  
        }
    }
    
