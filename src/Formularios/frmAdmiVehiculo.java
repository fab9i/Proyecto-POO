    void listarVehiculos(){
        modelo.setRowCount(0);
        for (int i = 0; i < gestionVehiculos.obtenerTotalVehiculos(); i++) {
            //crear filas
            Object[] fila = {
                gestionVehiculos.obtenerVehiculos()[i].getCodigo(),
                gestionVehiculos.obtenerVehiculos()[i].getMarca(),
                gestionVehiculos.obtenerVehiculos()[i].getModelo(),
                gestionVehiculos.obtenerVehiculos()[i].getColor(),
                gestionVehiculos.obtenerVehiculos()[i].getAnioFabricacion(),
                gestionVehiculos.obtenerVehiculos()[i].getTipo(),
                gestionVehiculos.obtenerVehiculos()[i].getPrecio(),
                gestionVehiculos.obtenerVehiculos()[i].getEstado(),
                
            };
            modelo.addRow(fila);
            
        }
    
    }
    
