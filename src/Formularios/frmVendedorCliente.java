    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String dni =txtDni.getText().trim();
        if(gestionClientes.eliminar(dni)){
            listarClientes();
            limpiar();
            JOptionPane.showMessageDialog(this, "Cliente eliminado");
        }else{
             JOptionPane.showMessageDialog(this, "No se encontr처 el cliente a eliminar");
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String dni =txtDni.getText().trim();
        String nombre = txtNombre.getText().trim();
        String apellido = txtApellido.getText().trim();
        String direccion = txtDireccion.getText().trim();
        String celular = txtCelular.getText().trim();
        String correoElectronico = txtCorreo.getText().trim();
        
        //Instanciamos
        Cliente cliente = new Cliente(dni, nombre, apellido, direccion, celular, correoElectronico);
        
        if(gestionClientes.modificar(cliente)){
            listarClientes();
            limpiar();
        }else{
            JOptionPane.showMessageDialog(this, "No se pudo registrar");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

